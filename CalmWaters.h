/**
 * @file CalmWaters.h
 * @brief Главный класс игры "Тихие Воды" (Calm Waters - Fishing Simulator)
 * @author Ищенко Кирилл
 * @version 1.0
 * @date 2026
 *
 * @mainpage Документация игры "Тихие Воды"
 *
 * Тихие Воды - это симулятор рыбалки на SDL3, написанный на C++20.
 *
 * @section features Основные особенности:
 * - Симуляция рыбалки с реалистичной механикой
 * - 6 видов рыб с разными характеристиками
 * - Мини-игра натяжения лески при вытягивании
 * - Система магазина с покупкой скинов и локаций
 * - Красивая графика с анимациями
 * - Фоновая музыка и звуковые эффекты
 *
 * @section controls Управление:
 * - **A/D** - движение лодки влево/вправо
 * - **Стрелки** - альтернативное движение
 * - **Пробел** - заброс/вытягивание рыбы
 * - **ЛКМ** - заброс в точку мыши
 * - **TAB** - открыть магазин
 * - **ESC** - выход из игры
 *
 * @section game_loop Главный игровой цикл:
 * 1. handleEvents() - обработка событий (закрытие, ввод)
 * 2. update() - обновление логики (движение, рыбалка, анимации)
 * 3. render() - отрисовка всего на экран
 * 4. SDL_Delay(16) - ограничение FPS до ~60
 *
 * @section architecture Архитектура:
 * ```
 * CalmWaters (главный класс)
 *     |
 *     +-- Player (рыбак на лодке)
 *     |    |
 *     |    +-- FishingRod (удочка)
 *     |         |
 *     |         +-- Bobber (поплавок)
 *     |              |
 *     |              +-- Fish (информация о рыбе)
 *     |
 *     +-- InputHandler (обработка ввода)
 *     |
 *     +-- GameTimer (таймер)
 * ```
 */

#pragma once

#include <iostream>
#include <SDL3/SDL.h>
#include "Player.h"
#include "InputHandler.h"

 /**
  * @enum GameState
  * @brief Состояния главного меню/экрана игры
  */
enum class GameState {
    PLAYING,   ///< Игра идёт, рыбалка
    SHOP       ///< Открыт магазин
};

/**
 * @class CalmWaters
 * @brief Главный класс игры
 *
 * CalmWaters отвечает за:
 * - Инициализацию SDL3 и окна
 * - Управление игровым циклом
 * - Обработку событий
 * - Отрисовку фона, HUD и интерфейса
 * - Управление состояниями игры (PLAYING vs SHOP)
 * - Генерацию музыки в реальном времени
 *
 * @note Используется паттерн RAII для управления ресурсами
 */
class CalmWaters {
public:
    /**
     * @brief Конструктор
     *
     * Инициализирует облака для анимации фона.
     */
    CalmWaters();

    /**
     * @brief Деструктор
     *
     * Вызывает clean() для освобождения ресурсов.
     */
    ~CalmWaters();

    /**
     * @brief Инициализирует игру и создаёт окно
     *
     * Выполняет:
     * - Инициализацию SDL3
     * - Создание окна нужного размера
     * - Создание рендерера
     * - Инициализацию аудиопотока для музыки
     * - Инициализацию игрока
     *
     * @param title Название окна
     * @param width Ширина окна в пикселях
     * @param height Высота окна в пикселях
     * @return true если инициализация успешна, false иначе
     *
     * @example
     * @code
     * CalmWaters game;
     * if (game.init("Calm Waters", 800, 600)) {
     *     // игра инициализирована
     * }
     * @endcode
     */
    bool init(const char* title, int width, int height);

    /**
     * @brief Обрабатывает события от операционной системы
     *
     * Обрабатывает:
     * - Закрытие окна (SDL_EVENT_QUIT)
     * - Нажатие ESC (выход или закрытие магазина)
     * - Движение мыши
     * - Нажатия кнопок мыши
     *
     * @note Должно быть вызвано один раз за кадр
     */
    void handleEvents();

    /**
     * @brief Обновляет логику игры каждый кадр
     *
     * Обновляет:
     * - Анимации волн и облаков
     * - Логику игрока
     * - Счёт времени для всплывающих сообщений
     * - Переключение между PLAYING и SHOP
     * - Обработку покупок в магазине
     *
     * @note Должно быть вызвано один раз за кадр
     */
    void update();

    /**
     * @brief Отрисовывает весь кадр на экран
     *
     * Отрисовывает в этом порядке:
     * 1. Фон (небо, облака, вода, деревья, берег)
     * 2. Игрока и удочку
     * 3. HUD (счёт, рыбы)
     * 4. Список улова (справа)
     * 5. Всплывающее сообщение о поймённой рыбе
     * 6. Статусная строка снизу (подсказки, натяжение)
     * 7. Магазин (если открыт)
     *
     * @note Должно быть вызвано один раз за кадр
     */
    void render();

    /**
     * @brief Очищает все ресурсы и выключает SDL3
     *
     * Освобождает:
     * - Игрока
     * - Аудиопоток
     * - Рендерер
     * - Окно
     * - SDL3
     *
     * @note Автоматически вызывается деструктором
     */
    void clean();

    /**
     * @brief Проверяет, работает ли игра
     *
     * @return true если игра продолжает работу, false если нужно выйти
     *
     * @note Используется в главном цикле: while (game.running())
     */
    bool running() const { return isRunning; }

private:
    // ─────────────────────────────────────────────────────────────────────
    //   CORE
    // ─────────────────────────────────────────────────────────────────────

    /// @brief Время последнего обновления в миллисекундах
    Uint64 lastTick = 0;

    /// @brief Флаг: работает ли игра
    bool isRunning = false;

    /// @brief Окно SDL3
    SDL_Window* window = nullptr;

    /// @brief Рендерер SDL3
    SDL_Renderer* renderer = nullptr;

    /// @brief Игрок (рыбак на лодке)
    Player* player = nullptr;

    /// @brief Обработчик пользовательского ввода
    InputHandler inputHandler;

    /// @brief Текущее состояние игры (PLAYING или SHOP)
    GameState gameState = GameState::PLAYING;

    // ─────────────────────────────────────────────────────────────────────
    //   INPUT
    // ─────────────────────────────────────────────────────────────────────

    /// @brief X-координата мыши
    float mouseX = 0;

    /// @brief Y-координата мыши
    float mouseY = 0;

    /// @brief Флаг: нажата ли ЛКМ в этом кадре
    bool mouseClick = false;

    // ─────────────────────────────────────────────────────────────────────
    //   ANIMATION & EFFECTS
    // ─────────────────────────────────────────────────────────────────────

    /// @brief Смещение волн для анимации (0..70)
    float waveOffset = 0;

    /// @brief Таймер мигания "КЛЕВ!" сообщения
    float biteFlashTimer = 0;

    /// @brief Флаг видимости "КЛЕВ!" сообщения при мигании
    bool biteVisible = true;

    /// @brief Анимация всплывающего сообщения о поймённой рыбе (0..4)
    float catchAnim = 0;

    /// @brief Текст всплывающего сообщения
    std::string catchMsg;

    /// @brief Таймер отображения "ЛЕСЦА ОБОРВАЛА" (0..2.5)
    float lineBreakTimer = 0;

    /// @struct Cloud
    /// @brief Структура облака для анимации
    struct Cloud {
        float x;      ///< X-позиция облака
        float y;      ///< Y-позиция облака
        float w;      ///< Ширина облака
        float speed;  ///< Скорость движения облака
    };

    /// @brief Массив облаков для анимации фона
    Cloud clouds[6];

    // ─────────────────────────────────────────────────────────────────────
    //   SHOP
    // ─────────────────────────────────────────────────────────────────────

    /// @brief Флаг: была ли TAB нажата в прошлом кадре
    bool shopKeyWas = false;

    // ─────────────────────────────────────────────────────────────────────
    //   AUDIO
    // ─────────────────────────────────────────────────────────────────────

    /// @brief Аудиопоток для воспроизведения музыки
    SDL_AudioStream* audioStream = nullptr;

    /// @brief Фазы для синтеза 4 нот музыки
    float audioPhase[4] = {};

    /// @brief Таймер музыки (для синхронизации мелодии)
    float musicTimer = 0;

    /// @brief Индекс текущей ноты в мелодии
    int noteIdx = 0;

    /**
     * @brief Генерирует музыку в реальном времени
     *
     * Синтезирует простую мелодию с использованием синусовых волн.
     * Добавляет гармоники и шум для естественности.
     *
     * @param dt Дельта времени в секундах
     *
     * @note Вызывается в update() каждый кадр
     */
    void generateMusic(float dt);

    /// @brief Массив высот нот в герцах (частоты)
    static const float NOTES[];

    // ─────────────────────────────────────────────────────────────────────
    //   RENDERING HELPERS
    // ─────────────────────────────────────────────────────────────────────

    /**
     * @brief Отрисовывает фон игры
     *
     * Рисует:
     * - Небо (с градиентом в зависимости от локации)
     * - Солнце или луну
     * - Облака
     * - Холмы
     * - Берег с деревьями
     * - Воду с волнами и лилиями
     *
     * @note Цвета и элементы зависят от player->location
     */
    void renderBg();

    /**
     * @brief Отрисовывает HUD игры
     *
     * Показывает:
     * - Очки (ОЧКИ: 150)
     * - Количество рыб (РЫБ: 3)
     * - Подсказку о TAB (магазин)
     *
     * @note Вызывает также renderStatusBar()
     */
    void renderHUD();

    /**
     * @brief Отрисовывает строку состояния внизу экрана
     *
     * Показывает:
     * - Подсказки управления (в зависимости от состояния)
     * - Прогресс ожидания клева
     * - Шкалу натяжения при вытягивании
     * - Сообщение об ошибке разрыва лески
     */
    void renderStatusBar();

    /**
     * @brief Отрисовывает шкалу натяжения лески
     *
     * Показывает трёхцветную шкалу:
     * - Зелёная (безопасно)
     * - Жёлтая (опасно)
     * - Красная (очень опасно)
     *
     * С индикатором текущего натяжения.
     */
    void renderTensionBar();

    /**
     * @brief Отрисовывает всплывающее сообщение о поймённой рыбе
     *
     * Показывает "ПОЙМАНА!" и название + вес рыбы.
     * Исчезает через 4 секунды.
     *
     * @param dt Дельта времени (не используется, оставлено для совместимости)
     */
    void renderCatchPopup(float dt);

    /**
     * @brief Отрисовывает список улова справа
     *
     * Показывает последние 7 пойманных рыб.
     *
     * @note Вызывается только в состоянии PLAYING
     */
    void renderCatchList();

    /**
     * @brief Отрисовывает интерфейс магазина
     *
     * Показывает:
     * - Кнопки для покупки скинов лодки
     * - Кнопки для покупки скинов удочки
     * - Кнопки для покупки локаций
     * - Текущее количество очков
     *
     * @note Вызывается только когда gameState == SHOP
     */
    void renderShop();

    // ─────────────────────────────────────────────────────────────────────
    //   CONSTANTS
    // ─────────────────────────────────────────────────────────────────────

    /// @brief Ширина окна в пикселях
    static constexpr int WIN_W = 800;

    /// @brief Высота окна в пикселях
    static constexpr int WIN_H = 600;

    /// @brief Y-координата уровня воды (граница между небом и водой)
    static constexpr float WATER_Y = 295.0f;
};