/**
 * @file GameTimer.h
 * @brief Простой таймер для отслеживания времени в игре
 * @author Разработчик игры
 * @version 1.0
 * @date 2024
 *
 * Этот модуль содержит класс GameTimer, который используется для отслеживания
 * времени между событиями, задержек клева рыбы и других временных операций в игре.
 */

#pragma once

#include <SDL3/SDL.h>

 /**
  * @class GameTimer
  * @brief Класс для управления временем событий в игре
  *
  * Используется для:
  * - Отслеживания времени ожидания клева рыбы
  * - Проверки прошедшего времени между событиями
  * - Контроля анимационных циклов
  */
class GameTimer {
public:
    /**
     * @brief Запускает таймер
     *
     * Сохраняет текущее время в миллисекундах. Все дальнейшие
     * вычисления выполняются относительно этого момента.
     */
    void start();

    /**
     * @brief Проверяет, прошло ли заданное время
     *
     * @param seconds Количество секунд для проверки
     * @return true если прошло хотя бы столько времени, false иначе
     *
     * @example
     * @code
     * timer.start();
     * // ... какой-то код
     * if (timer.hasElapsed(5.0f)) {
     *     std::cout << "Прошло 5 секунд!";
     * }
     * @endcode
     */
    bool hasElapsed(float seconds) const;

    /**
     * @brief Получает прошедшее время в секундах
     *
     * @return Количество секунд, прошедших с момента запуска таймера
     *
     * @note Возвращаемое значение зависит от того, был ли таймер запущен.
     *       Если start() не вызывался, вернёт большое число.
     */
    float getElapsed() const;

    /**
     * @brief Перезагружает таймер
     *
     * Эквивалентно повторному вызову start().
     * Полезно для перезапуска отсчёта времени.
     */
    void reset() { start(); }

private:
    /// @brief Время запуска в миллисекундах
    Uint64 startTick = 0;
};