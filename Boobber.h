/**
 * @file Boobber.h
 * @brief Класс поплавка и логика его поведения
 * @author Ищенко Кирилл
 * @version 1.0
 * @date 2026
 *
 * Этот модуль содержит класс Bobber, который управляет поплавком и всей
 * логикой рыбалки: заброс, ожидание клева, вытягивание и натяжение лески.
 */

#pragma once

#include <SDL3/SDL.h>
#include "GameTimer.h"
#include "Fish.h"

 /**
  * @enum BobberState
  * @brief Состояния поплавка и процесса рыбалки
  *
  * Представляет текущую фазу процесса рыбалки.
  * Каждое состояние имеет свою логику обновления и отрисовки.
  */
enum class BobberState {
    IDLE,      ///< Поплавок в покое у лодки
    FLYING,    ///< Поплавок летит к точке заброса
    FLOATING,  ///< Поплавок плывёт, ждём клева рыбы
    BITING,    ///< Рыба клюёт! Игрок должен нажать пробел
    REELING,   ///< Игрок вытягивает рыбу (мини-игра натяжения)
    CAUGHT     ///< Рыба успешно поймана
};

/**
 * @class Bobber
 * @brief Класс для управления поплавком и логикой рыбалки
 *
 * Bobber отвечает за:
 * - Анимацию полёта поплавка при забросе
 * - Покачивание на воде при ожидании
 * - Обработку клева рыбы
 * - Мини-игру натяжения лески
 * - Отрисовку поплавка с учётом состояния
 *
 * Класс использует GameTimer для отслеживания времени и Fish для
 * хранения информации о поймённой рыбе.
 */
class Bobber {
public:
    /**
     * @brief Инициализирует поплавок
     *
     * Устанавливает начальное положение поплавка (у лодки).
     *
     * @param x X-координата домашней позиции (у лодки)
     * @param y Y-координата домашней позиции
     *
     * @note Это должно быть вызвано один раз при создании поплавка
     */
    void init(float x, float y);

    /**
     * @brief Заброс поплавка в конкретную точку на воде
     *
     * Запускает анимацию полёта поплавка к целевой точке.
     * После прибытия в точку начинается ожидание клева.
     * Автоматически генерируется случайная рыба.
     *
     * @param tx X-координата целевой точки
     * @param ty Y-координата целевой точки (будет скорректирована если выше воды)
     *
     * @note Y-координата будет автоматически установлена в воду если слишком высокая
     *
     * @example
     * @code
     * bobber.castTo(400.0f, 330.0f); // Заброс в точку (400, 330)
     * @endcode
     */
    void castTo(float tx, float ty);

    /**
     * @brief Случайный заброс поплавка
     *
     * Выполняет заброс в случайную точку перед игроком.
     * Используется при нажатии на пробел для простого управления.
     *
     * @param rodX X-координата удочки
     * @param rodY Y-координата удочки
     *
     * @example
     * @code
     * bobber.castRandom(rodX, rodY);
     * @endcode
     */
    void castRandom(float rodX, float rodY);

    /**
     * @brief Обновляет логику поплавка каждый кадр
     *
     * Обновляет состояние в зависимости от текущей фазы:
     * - FLYING: анимирует полёт по параболе
     * - FLOATING: качается на воде, проверяет время клева
     * - BITING: дёргается интенсивно, ждёт нажатия игрока
     * - REELING: движется к лодке, отслеживает натяжение
     *
     * @param dt Дельта времени в секундах (обычно 1/60)
     *
     * @note Это должно быть вызвано один раз за кадр
     */
    void update(float dt);

    /**
     * @brief Отрисовывает поплавок на экран
     *
     * Рисует поплавок в виде оранжевого/красного круга с разными
     * цветами в зависимости от состояния:
     * - FLOATING: жёлтый
     * - BITING: красный (интенсивно мигает)
     * - REELING: цвет зависит от натяжения лески
     *
     * @param renderer SDL рендерер для отрисовки
     */
    void render(SDL_Renderer* renderer);

    /**
     * @brief Начинает мини-игру вытягивания рыбы
     *
     * Переводит поплавок в состояние REELING.
     * Должно быть вызвано только когда поплавок находится в состоянии BITING.
     *
     * @note Игрок должен удерживать пробел для ослабления натяжения
     */
    void startReeling();

    /**
     * @brief Ослабляет натяжение лески при нажатии игроком
     *
     * Вызывается когда игрок удерживает пробел в состоянии REELING.
     * Уменьшает натяжение лески (tension).
     *
     * @param dt Дельта времени в секундах
     *
     * @note Это специфично для мини-игры вытягивания
     */
    void pullLine(float dt);

    /**
     * @brief Сбрасывает состояние после поимки или потери рыбы
     *
     * Перемещает поплавок обратно в состояние IDLE, очищает флаги.
     * Должно быть вызвано после того как рыба была доставлена или потеряна.
     */
    void resetCatch();

    /**
     * @brief Проверяет, поймана ли рыба
     *
     * @return true если рыба успешно поймана, false иначе
     */
    bool isCaught() const;

    /**
     * @brief Получает информацию о поймённой рыбе
     *
     * @return Структура Fish с информацией о рыбе
     *
     * @warning Используйте только после проверки isCaught() == true
     */
    Fish getCaughtFish() const;

    /**
     * @brief Получает текущее состояние поплавка
     *
     * @return Текущее состояние (BobberState)
     */
    BobberState getState() const { return state; }

    /**
     * @brief Получает X-позицию центра поплавка
     *
     * @return X-координата в пикселях
     */
    float getPosX() const { return rect.x + rect.w * 0.5f; }

    /**
     * @brief Получает Y-позицию центра поплавка
     *
     * @return Y-координата в пикселях
     */
    float getPosY() const { return rect.y + rect.h * 0.5f; }

    /**
     * @brief Получает прогресс ожидания клева (0..1)
     *
     * В состоянии FLOATING возвращает отношение прошедшего времени к времени клева.
     * В других состояниях возвращает 0.
     *
     * @return Значение от 0.0 до 1.0 (0 = только забросили, 1.0 = пора клёв)
     */
    float getBiteProgress() const;

    /**
     * @brief Получает натяжение лески (0..1)
     *
     * В состоянии REELING показывает, насколько лесна натянута.
     * 0 = не натянута, 1.0 = порвалась.
     *
     * @return Текущее натяжение
     */
    float getTension() const { return tension; }

    /**
     * @brief Проверяет, порвалась ли леса
     *
     * @return true если леса порвалась из-за перенатяжения
     */
    bool isLineBroken() const { return lineBroken; }

private:
    /// @brief Прямоугольник поплавка (позиция и размер)
    SDL_FRect rect = { 0, 0, 14, 14 };

    /// @brief X-координата дома (у лодки)
    float homeX = 0;

    /// @brief Y-координата дома (у лодки)
    float homeY = 0;

    /// @brief Целевая X-координата (куда летит/плывёт поплавок)
    float targetX = 0;

    /// @brief Целевая Y-координата
    float targetY = 0;

    /// @brief Прогресс полёта (0..1)
    float flyProgress = 0.0f;

    /// @brief Стартовая X для расчёта дуги полёта
    float startX = 0;

    /// @brief Стартовая Y для расчёта дуги полёта
    float startY = 0;

    /// @brief Текущее состояние поплавка
    BobberState state = BobberState::IDLE;

    /// @brief Таймер для отслеживания времени до клева
    GameTimer waitTimer;

    /// @brief Рыба, которая может клюнуть (генерируется при забросе)
    Fish pendingFish;

    /// @brief Смещение при покачивании на воде
    float bobOffset = 0;

    /// @brief Переменная анимации покачивания
    float bobTime = 0;

    /// @brief Флаг: поймана ли рыба
    bool caught = false;

    /// @brief Натяжение лески при вытягивании (0..1)
    float tension = 0.0f;

    /// @brief Флаг: порвалась ли леса
    bool lineBroken = false;

    /// @brief Таймер ослабления (если нет нажатий)
    float slack = 0.0f;

    /**
     * @brief Внутренний метод выполнения заброса
     *
     * @param tx Целевая X
     * @param ty Целевая Y
     *
     * @note Это внутренний метод, используется castTo и castRandom
     */
    void docast(float tx, float ty);
};