/**
 * @file Fish.h
 * @brief Структура и логика для описания рыб в игре
 * @author Разработчик игры
 * @version 1.0
 * @date 2024
 *
 * Этот модуль содержит определение различных видов рыб, их характеристик
 * и методы для генерирования случайных рыб с разными свойствами.
 */

#pragma once

#include <string>

 /**
  * @enum FishType
  * @brief Перечисление видов рыб в игре
  *
  * Каждый вид имеет уникальный вес, скорость клева и стоимость в очках.
  * Более редкие рыбы дают больше очков.
  */
enum class FishType {
    CARP,      ///< Карп - тяжёлая рыба
    PERCH,     ///< Окунь - лёгкая рыба
    PIKE,      ///< Щука - очень тяжёлая рыба
    BREAM,     ///< Лещ - среднего размера
    CRUCIAN,   ///< Карась - лёгкая рыба
    CATFISH    ///< Сом - легендарно тяжёлая рыба
};

/**
 * @struct Fish
 * @brief Структура, описывающая характеристики рыбы
 *
 * Содержит всю информацию о конкретной рыбе:
 * тип, вес, время ожидания клева и редкость.
 */
struct Fish {
    /// @brief Тип рыбы
    FishType type = FishType::CARP;

    /// @brief Вес рыбы в килограммах
    float weight = 0.0f;

    /// @brief Время ожидания до клева в секундах
    float biteDelay = 0.0f;

    /// @brief Название рыбы
    std::string name;

    /// @brief Редкость рыбы (1=обычная, 2=редкая, 3=легендарная)
    int rarity = 1;

    /**
     * @brief Генерирует случайную рыбу
     *
     * Вероятность появления зависит от редкости:
     * - 35% - Окунь (обычный)
     * - 25% - Карась (обычный)
     * - 18% - Карп (редкий)
     * - 12% - Лещ (редкий)
     * - 8% - Щука (редкая)
     * - 2% - Сом (легендарный)
     *
     * @return Новый экземпляр Fish со случайными характеристиками
     *
     * @example
     * @code
     * Fish randomFish = Fish::random();
     * std::cout << "Поймал: " << randomFish.name << std::endl;
     * @endcode
     */
    static Fish random();

    /**
     * @brief Преобразует рыбу в строковое представление
     *
     * Возвращает строку формата "Название Вес кг"
     * (например: "Karp 2.3kg")
     *
     * @return Строка с названием и весом рыбы
     */
    std::string toString() const;

    /**
     * @brief Вычисляет количество очков за эту рыбу
     *
     * Формула: вес × множитель × 10
     *
     * Множители по типам:
     * - Сом: ×5.0 (тяжелейшая)
     * - Щука: ×3.0 (тяжёлая)
     * - Карп: ×2.0 (средняя)
     * - Лещ: ×1.5 (средняя)
     * - Карась: ×1.2 (лёгкая)
     * - Окунь: ×1.0 (базовая)
     *
     * @return Количество очков за поимку этой рыбы
     *
     * @example
     * @code
     * Fish pike = Fish::random();
     * int points = pike.getScore();
     * std::cout << "Получил " << points << " очков!" << std::endl;
     * @endcode
     */
    int getScore() const;
};